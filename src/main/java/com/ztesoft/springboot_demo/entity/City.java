package com.ztesoft.springboot_demo.entity;

public class City {
	private int citid;
	private String citname;
	private int proid;

	public int getCitid() {
		return citid;
	}

	public void setCitid(int citid) {
		this.citid = citid;
	}

	public String getCitname() {
		return citname;
	}

	public void setCitname(String citname) {
		this.citname = citname;
	}

	public int getProid() {
		return proid;
	}

	public void setProid(int proid) {
		this.proid = proid;
	}
}
