package com.ztesoft.demo.springboot_demo.entity;

public class Region {
	private int regid;
	private String regname;
	private int citid;

	public int getRegid() {
		return regid;
	}

	public void setRegid(int regid) {
		this.regid = regid;
	}

	public String getRegname() {
		return regname;
	}

	public void setRegname(String regname) {
		this.regname = regname;
	}

	public int getCitid() {
		return citid;
	}

	public void setCitid(int citid) {
		this.citid = citid;
	}
}
